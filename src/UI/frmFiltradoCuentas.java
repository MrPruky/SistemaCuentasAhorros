    private void btnEditarCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarCuentaActionPerformed
        int filaSeleccionada = jTable1.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una cuenta para editar.", "Ninguna Fila Seleccionada", JOptionPane.WARNING_MESSAGE);
        } else {
            // Obtener el ID de la cuenta de la primera columna (columna 0) de la fila seleccionada
            // Aseg√∫rate de que el ID es un Integer o de tipo compatible con int
            int idCuentaAEditar = (int) jTable1.getModel().getValueAt(filaSeleccionada, 0);

            Cuenta cuentaAEditar = new Cuenta();
            cuentaAEditar.setIdCuenta(idCuentaAEditar);

            // Intentar buscar la cuenta en la base de datos para cargar todos sus datos
            if (cuentaAEditar.buscar()) {
                // Crear una instancia de frmCuentas (tu formulario de registro/edici√≥n)
                frmCuentas formCuentas = new frmCuentas(null, true);
                // Llamar a un m√©todo en frmCuentas para cargar los datos de la cuenta en sus campos
               formCuentas.cargarDatosCuentaParaEdicion(cuentaAEditar); // Llama al m√©todo correcto
                formCuentas.setVisible(true);
                // Despu√©s de que frmCuentas se cierra (ya sea por guardar o cancelar), recargar la tabla
                cargarCuentasEnTabla();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo encontrar la cuenta seleccionada en la base de datos.", "Error de B√∫squeda", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditarCuentaActionPerformed

    private void btnEliminarCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCuentaActionPerformed
        int filaSeleccionada = jTable1.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una cuenta para eliminar.", "Ninguna Fila Seleccionada", JOptionPane.WARNING_MESSAGE);
        } else {
            // Obtener el ID de la cuenta de la primera columna (columna 0) de la fila seleccionada
            int idCuentaAEliminar = (int) jTable1.getModel().getValueAt(filaSeleccionada, 0);

            // Confirmar la eliminaci√≥n con el usuario
            int confirmacion = JOptionPane.showConfirmDialog(this,
                    "¬øEst√° seguro de que desea eliminar la cuenta con ID " + idCuentaAEliminar + "?\nEsta acci√≥n es irreversible.",
                    "Confirmar Eliminaci√≥n", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (confirmacion == JOptionPane.YES_OPTION) {
                Cuenta cuentaAEliminar = new Cuenta();
                cuentaAEliminar.setIdCuenta(idCuentaAEliminar);

                // Llamar al m√©todo eliminar() de tu clase Cuenta
                if (cuentaAEliminar.eliminar()) {
                    JOptionPane.showMessageDialog(this, "Cuenta eliminada exitosamente.", "√âxito", JOptionPane.INFORMATION_MESSAGE);
                    cargarCuentasEnTabla(); // Recargar la tabla para mostrar los cambios
                } else {
                    JOptionPane.showMessageDialog(this, "Error al eliminar la cuenta.\nVerifique la conexi√≥n a la base de datos o si la cuenta est√° relacionada con otros registros.", "Error de Eliminaci√≥n", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnEliminarCuentaActionPerformed

